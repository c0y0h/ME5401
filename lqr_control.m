function [K] = lqr_control(A, B)
%LQR_CONTROL 此处显示有关此函数的摘要
%   此处显示详细说明
Q = [50   0   0   0   0   0;
      0  50   0   0   0   0;
      0   0  50   0   0   0;
      0   0   0  50   0   0;
      0   0   0   0  50   0;
      0   0   0   0   0  50];
R = [10   0;
     0   10];
M = [ A   -B/R*B';
     -Q   -A'];

[X, D] = eig(M);
U = X(1:6, 1:6);
V = X(7:12, 1:6);
P = U/V;

K = R \ B' * P;

end

